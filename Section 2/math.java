
public class math {

	public static void main(String[] args) {
		
		//int , double 정수랑 실수를 표현하는 TYPE들을 배웠어요
		
		int a = 9;
		int b = 10;
		
		System.out.println(a + b);	//덧셈
		System.out.println(a - b);	//뺄셈
		System.out.println(a * b);	//곱셈
		System.out.println(a / b); 	//나눗셈
		System.out.println(b % a);	//나머지?		10 / 9의 나머지를 출력해줘 1

		//문자열과 문자열을 더할 수 있다는게 이해가 되겠죠
		
		System.out.println("안녕하세요" + "허태훈입니다");
		
	}

}
